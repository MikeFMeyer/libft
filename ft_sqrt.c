#include "libft.h"

size_t	ft_sqrt(size_t num)
{
	size_t i;

	if (num <= 0)
		return (0);
	i = 1;
	while ((i * i) <= num)
	{
		if ((i * i) == num)
			return (i);
		i++;
	}
	return (0);
}
